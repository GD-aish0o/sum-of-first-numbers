// - Write a for loop to find the sum of the first 10 numbers.

// here's a simple code


let sum = 0;

// feel free to edit * the number below this steroid.
for (let i = 1; i <= 10; i++) {


    // The += operator is used for assignment with addition. It combines the value on the left-hand side with the value on the right-hand side and assigns the result back to the left-hand side variable. 

    // Take this as an example: 
    // sum=5; 
//  sum += 3; after this whenever you print the sun variable it will return 8. 
    sum += i;
}

console.log(`The sum of the first 10 numbers is: ${sum}`);